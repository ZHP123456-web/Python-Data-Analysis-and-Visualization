import numpy as np

arr = np.array(5) #创建0维ndarray数组
print(arr)
print('arr的维度：',arr.ndim) #数组的维度numpy of dimensions

arr = np.array([1,2,3]) #创建1维ndarray数组
print(arr)
print('arr的维度：',arr.ndim) #数组的维度numpy of dimensions

arr = np.array([[1,2,3],[4,5,6]]) #创建2维ndarray数组
print(arr)
print('arr的维度：',arr.ndim) #数组的维度numpy of dimensions